"""
Guarantee value of <variable>_<Max|Min|First|Last> has to match BinMatrix.flm.usrv values. Only variables with _Max|_Min|_First|_Last will be checked.
The BinMatrix.flm.usrv is automatically generated by BinSplit team, but the BaseSpecs.usrv is manually updated.
Link: https://wiki.ith.intel.com/display/ITSpdxtp/BinMatrix+UserVars

"""
import sys
import setenv

from gadget.dictmore import xmlfunc, iter_dot_items, find_dot_items
from gadget.pylog import log
from qgates.qgate_base import QGateBase
from tp.testprogram import TestProgram
from pprint import pprint
